; Reset Handler

Reset_Handler   PROC
                EXPORT  Reset_Handler             [WEAK]


				mov r0, #-10
				mvn r0, #10

				LDR r4, =0x424B0000 
				LDR r5, =0x41A40000

				;stop timer
				LDR r0, =CSR
				MOV r1, #0
				STR r1, [r0]
				;set reload value register to number of ck cycles between interr
				LDR r2, =RVR
				mov r3, #0x80000
				STR r3, [r2]
				;store in current value register whichever number to clear its content
				LDR r2, =CVR
				STR r3, [r2]
				; set bit 0, 1 and 2 to 1 to start timer
				mov r1, #7
				STR r1, [r0]

stop			B stop

                ENDP


