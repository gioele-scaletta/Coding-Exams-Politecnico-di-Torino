addFPpositiveNumbers PROC ; I consider number 1 in r0 and number 2 in r1
				
				PUSH {r4,r5,r6,r7,r8,LR} 
				
				MOV r2, r0, lsl #9 ;point 1
				MOV r3, r1, lsl #9
				LSR r2, #9
				LSR r3, #9
				ORR r2, r2, #0x800000 ;setting bit 23 to 1 (point 2)
				ORR r3, r3, #0x800000
				MOV r4, r0, lsr #23 ; since exp is 0 like this is enough (point 3)
				MOV r5, r1, lsr #23

				cmp r4, r5				
				BEQ same_exp ;if r4=r5 just put one of them in r6 as result exp
				BHI first_higher
				
second_higher			mov r6, r5 ; store in r6 result exp
				sub r7, r5, r4
				lsr r2, r7   
				B cont
				
first_higher    		mov r6, r4; if one of them is higher store in r7 number of bit to shift and shift
				sub r7, r4, r5
				lsr r3, r7   
				B cont
				
same_exp       			mov r6, r4

cont				ADD r7, r2, r3 ; result mantissa in r7    
				eor r8,r8
				mov r8, r7, lsr #24 ;changed number

				cmp r8, #1
				bne isnotone	
			
				lsr r7, #1
				add r6, #1
				
isnotone			BIC r7, #0x800000 ;point 5
				lsl r6, #23 ;point 6
				add r0, r6, r7 ;return result in r0

				POP {r4,r5,r6,r7,r8, PC} 
				
				ENDP

